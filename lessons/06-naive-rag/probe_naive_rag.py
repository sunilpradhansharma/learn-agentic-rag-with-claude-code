"""
probe_naive_rag.py — Run 10 diagnostic questions through the naive RAG
system and write results to probe_results.md for manual grading.

The questions are chosen to surface specific failure modes:
  - Q1,Q3,Q7: specific numeric or factual questions
  - Q2,Q6:    detail questions that require exact retrieval
  - Q4:       comparative (two companies) — likely partial failure
  - Q5:       multi-item list — depends on retrieval coverage
  - Q8:       out-of-corpus year — should refuse
  - Q9:       simple yes/no fact
  - Q10:      completely out-of-corpus — should refuse clearly

Run:
  python lessons/06-naive-rag/probe_naive_rag.py
"""

import os
import sys
import textwrap

# Add src/rag/ to path so we can import NaiveRAG.
_REPO_ROOT = os.path.normpath(
    os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "..")
)
sys.path.insert(0, os.path.join(_REPO_ROOT, "src", "rag"))

from naive_rag import NaiveRAG  # noqa: E402

# ---------------------------------------------------------------------------
# Probe questions
# ---------------------------------------------------------------------------

QUESTIONS = [
    "What was Apple's total net sales in fiscal 2023?",
    "What is Tesla's primary manufacturing location in the US?",
    "What percentage of Microsoft's revenue comes from cloud?",
    "Compare Apple's 2023 revenue to Tesla's 2023 revenue.",
    "What are the top three risk factors Apple lists?",
    "Who serves on Tesla's board of directors?",
    "How many employees does Microsoft have?",
    "What was Apple's revenue in 2019?",
    "Does Tesla pay a dividend?",
    "What is the weather today in San Francisco?",
]

# Where to write results — lives in the lesson directory so students see it.
_LESSON_DIR = os.path.dirname(os.path.abspath(__file__))
OUTPUT_PATH = os.path.join(_LESSON_DIR, "probe_results.md")


def truncate(text: str, max_chars: int = 300) -> str:
    """Trim text and append '…' if it exceeds max_chars."""
    text = text.replace("\n", " ").strip()
    if len(text) <= max_chars:
        return text
    return text[:max_chars] + "…"


def escape_pipe(text: str) -> str:
    """Escape pipe characters so they don't break the markdown table."""
    return text.replace("|", "&#124;")


def main() -> None:
    print("Loading NaiveRAG …")
    rag = NaiveRAG(k=5)

    if rag.store.count() == 0:
        print("Vector store is empty. Run `python src/rag/vector_store.py` first.")
        sys.exit(1)

    print(f"Running {len(QUESTIONS)} probe questions …\n")

    rows = []
    for i, question in enumerate(QUESTIONS, 1):
        print(f"  Q{i}: {question[:60]} …", flush=True)
        result = rag.answer(question)

        # Unique source files retrieved, sorted for determinism.
        sources = sorted({c["source_file"] for c in result["retrieved_chunks"]})
        sources_str = ", ".join(s.replace("_10k_2023.txt", "") for s in sources)

        rows.append(
            {
                "num": i,
                "question": question,
                "answer": truncate(result["answer"], 300),
                "sources": sources_str,
            }
        )

    # ------------------------------------------------------------------
    # Write probe_results.md
    # ------------------------------------------------------------------
    header = textwrap.dedent("""\
        # Lesson 6 — Probe Results

        Fill in the **Pass/Fail/Partial** and **Failure mode** columns after
        reading each answer.

        Failure modes (from the Concepts section of the lesson README):
        - **wrong retrieval** — top-k chunks don't contain the answer
        - **partial retrieval** — answer spans chunks but only some retrieved
        - **hallucination** — model fills in gaps despite grounding instructions
        - **citation error** — model attributes facts to the wrong source
        - **comparative** — question requires data from two filings
        - **numerical precision** — right chunk retrieved, number misread
        - **out-of-corpus** — year or company not in the corpus

        ---

    """)

    # Table header
    col_sep = " | "
    columns = ["Q#", "Question", "Answer (truncated)", "Retrieved Sources",
               "Pass/Fail/Partial", "Failure mode"]
    header_row = col_sep.join(columns)
    separator_row = col_sep.join(["---"] * len(columns))

    lines = [header, f"| {header_row} |", f"| {separator_row} |"]

    for row in rows:
        cells = [
            str(row["num"]),
            escape_pipe(row["question"]),
            escape_pipe(row["answer"]),
            escape_pipe(row["sources"]),
            "",   # student fills in
            "",   # student fills in
        ]
        lines.append("| " + " | ".join(cells) + " |")

    lines.append("\n---\n")
    lines.append("*Generated by probe_naive_rag.py — fill in the last two columns manually.*\n")

    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"\nResults written to: {OUTPUT_PATH}")
    print("Open probe_results.md and fill in Pass/Fail/Partial and Failure mode columns.")


if __name__ == "__main__":
    main()
